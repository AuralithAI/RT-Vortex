// Package metrics provides Prometheus instrumentation for the RTVortex API server.
//
// All counters, histograms and gauges are declared here so that every other
// package can import metrics and call the recording helpers without pulling in
// the full prometheus API surface.
package metrics

import (
	"strconv"
	"time"

	"github.com/prometheus/client_golang/prometheus"
	"github.com/prometheus/client_golang/prometheus/promauto"
)

// ── Namespace / Subsystems ──────────────────────────────────────────────────

const ns = "rtvortex"

// ── HTTP Metrics ────────────────────────────────────────────────────────────

var (
	// HTTPRequestsTotal counts every HTTP request handled by the server.
	HTTPRequestsTotal = promauto.NewCounterVec(prometheus.CounterOpts{
		Namespace: ns,
		Subsystem: "http",
		Name:      "requests_total",
		Help:      "Total HTTP requests by method, path pattern, and status code.",
	}, []string{"method", "path", "status"})

	// HTTPRequestDuration observes request latency in seconds.
	HTTPRequestDuration = promauto.NewHistogramVec(prometheus.HistogramOpts{
		Namespace: ns,
		Subsystem: "http",
		Name:      "request_duration_seconds",
		Help:      "HTTP request duration in seconds.",
		Buckets:   prometheus.DefBuckets,
	}, []string{"method", "path", "status"})

	// HTTPRequestsInFlight tracks how many requests are being processed right now.
	HTTPRequestsInFlight = promauto.NewGauge(prometheus.GaugeOpts{
		Namespace: ns,
		Subsystem: "http",
		Name:      "requests_in_flight",
		Help:      "Number of HTTP requests currently being processed.",
	})
)

// ── Review Pipeline Metrics ─────────────────────────────────────────────────

var (
	// ReviewPipelineTotal counts completed (or failed) pipeline runs.
	ReviewPipelineTotal = promauto.NewCounterVec(prometheus.CounterOpts{
		Namespace: ns,
		Subsystem: "review",
		Name:      "pipeline_total",
		Help:      "Total review pipeline executions by status (completed, failed).",
	}, []string{"status"})

	// ReviewPipelineDuration observes pipeline execution time.
	ReviewPipelineDuration = promauto.NewHistogram(prometheus.HistogramOpts{
		Namespace: ns,
		Subsystem: "review",
		Name:      "pipeline_duration_seconds",
		Help:      "Review pipeline execution duration in seconds.",
		Buckets:   []float64{1, 5, 10, 30, 60, 120, 300, 600},
	})

	// ReviewCommentsTotal counts comments generated by reviews.
	ReviewCommentsTotal = promauto.NewCounter(prometheus.CounterOpts{
		Namespace: ns,
		Subsystem: "review",
		Name:      "comments_total",
		Help:      "Total review comments generated.",
	})

	// ReviewFilesAnalyzed counts files analyzed across all reviews.
	ReviewFilesAnalyzed = promauto.NewCounter(prometheus.CounterOpts{
		Namespace: ns,
		Subsystem: "review",
		Name:      "files_analyzed_total",
		Help:      "Total files analyzed across all review runs.",
	})
)

