// ── Recording Helpers ───────────────────────────────────────────────────────

// RecordHTTPRequest records an HTTP request's metrics.
func RecordHTTPRequest(method, path string, statusCode int, duration time.Duration) {
	status := strconv.Itoa(statusCode)
	HTTPRequestsTotal.WithLabelValues(method, path, status).Inc()
	HTTPRequestDuration.WithLabelValues(method, path, status).Observe(duration.Seconds())
}

// RecordEngineCall records a gRPC call to the engine.
func RecordEngineCall(method, status string, duration time.Duration) {
	EngineGRPCRequestsTotal.WithLabelValues(method, status).Inc()
	EngineGRPCDuration.WithLabelValues(method).Observe(duration.Seconds())
}

// RecordLLMRequest records an LLM completion request.
func RecordLLMRequest(provider, model, status string, duration time.Duration, promptTokens, completionTokens int) {
	LLMRequestsTotal.WithLabelValues(provider, model, status).Inc()
	LLMRequestDuration.WithLabelValues(provider, model).Observe(duration.Seconds())
	if promptTokens > 0 {
		LLMTokensUsed.WithLabelValues(provider, "prompt").Add(float64(promptTokens))
	}
	if completionTokens > 0 {
		LLMTokensUsed.WithLabelValues(provider, "completion").Add(float64(completionTokens))
	}
}

// RecordPipelineComplete records a review pipeline completion.
func RecordPipelineComplete(status string, duration time.Duration, commentsCount, filesCount int) {
	ReviewPipelineTotal.WithLabelValues(status).Inc()
	ReviewPipelineDuration.Observe(duration.Seconds())
	ReviewCommentsTotal.Add(float64(commentsCount))
	ReviewFilesAnalyzed.Add(float64(filesCount))
}
