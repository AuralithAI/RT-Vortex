// Package benchmark implements the A/B testing harness for comparing RTVortex
// swarm agent performance against single-agent baselines.  It drives identical
// tasks through both pipelines, collecting latency, correctness, LLM call
// counts, and quality scores.
package benchmark

import (
	"time"

	"github.com/google/uuid"
)

// ── Benchmark Mode ──────────────────────────────────────────────────────────

// Mode selects which agent pipeline processes the task.
type Mode string

const (
	ModeSwarm       Mode = "swarm"        // Full multi-agent swarm pipeline
	ModeSingleAgent Mode = "single_agent" // Golden baseline: one agent, no coordination
	ModeBoth        Mode = "both"         // A/B: run both and compare
)

// ── Benchmark Task ──────────────────────────────────────────────────────────

// Task represents a single benchmark task: a PR diff or synthetic coding task
// with optional ground-truth annotations.
type Task struct {
	ID          string            `json:"id"`
	Name        string            `json:"name"`
	Description string            `json:"description"`
	Category    string            `json:"category"`   // "real_pr" | "synthetic"
	Complexity  string            `json:"complexity"` // "simple" | "medium" | "complex" | "monolith"
	RepoURL     string            `json:"repo_url"`
	RepoID      string            `json:"repo_id"`
	DiffPatch   string            `json:"diff_patch,omitempty"` // unified diff for PR tasks
	Files       []TaskFile        `json:"files,omitempty"`      // for synthetic tasks
	Expected    *ExpectedResult   `json:"expected,omitempty"`   // ground truth
	Tags        []string          `json:"tags,omitempty"`
	Metadata    map[string]string `json:"metadata,omitempty"`
}

// TaskFile represents a file in a synthetic benchmark task.
type TaskFile struct {
	Path      string `json:"path"`
	Content   string `json:"content"`
	Language  string `json:"language"`
	IsChanged bool   `json:"is_changed"` // true if part of the "diff"
}

// ExpectedResult is the ground-truth annotation for scoring.
type ExpectedResult struct {
	Comments []ExpectedComment `json:"comments,omitempty"`
	Plan     string            `json:"plan,omitempty"`      // expected plan summary
	MinScore float64           `json:"min_score,omitempty"` // minimum acceptable quality score
}

// ExpectedComment is a ground-truth review comment annotation.
type ExpectedComment struct {
	File        string `json:"file"`
	Line        int    `json:"line,omitempty"`
	Severity    string `json:"severity"`    // "critical" | "warning" | "info"
	Category    string `json:"category"`    // "security" | "performance" | "logic" | "style"
	Pattern     string `json:"pattern"`     // regex or keyword to match in the generated comment
	Description string `json:"description"` // human-readable description of what should be found
}

// ── Run Request / Result ────────────────────────────────────────────────────

// RunRequest initiates a benchmark run.
type RunRequest struct {
	Name     string   `json:"name"`               // human-readable run name
	Mode     Mode     `json:"mode"`               // which pipeline(s) to test
	TaskIDs  []string `json:"task_ids,omitempty"` // specific tasks; empty = run all
	Category string   `json:"category,omitempty"` // filter by category
	Tags     []string `json:"tags,omitempty"`     // filter by tags
}

// Run tracks a complete benchmark execution.
type Run struct {
	ID        uuid.UUID   `json:"id"`
	Name      string      `json:"name"`
	Mode      Mode        `json:"mode"`
	Status    string      `json:"status"` // "running" | "completed" | "failed"
	StartedAt time.Time   `json:"started_at"`
	EndedAt   *time.Time  `json:"ended_at,omitempty"`
	Results   []RunResult `json:"results"`
	Summary   *RunSummary `json:"summary,omitempty"`
}

// RunResult holds the output from running a single task through one mode.
type RunResult struct {
	TaskID     string        `json:"task_id"`
	TaskName   string        `json:"task_name"`
	Mode       Mode          `json:"mode"`
	Status     string        `json:"status"` // "passed" | "failed" | "timeout" | "error"
	StartedAt  time.Time     `json:"started_at"`
	EndedAt    time.Time     `json:"ended_at"`
	LatencyMS  int64         `json:"latency_ms"`
	LLMCalls   int           `json:"llm_calls"`
	TokensUsed int           `json:"tokens_used"`
	Comments   []GenComment  `json:"comments,omitempty"`    // actual generated comments
	DiffOutput string        `json:"diff_output,omitempty"` // generated diff for swarm tasks
	Score      *QualityScore `json:"score,omitempty"`
	Error      string        `json:"error,omitempty"`
	Trace      []string      `json:"trace,omitempty"` // reasoning/debug trace
}

// GenComment is a comment generated by the agent.
type GenComment struct {
	File     string `json:"file"`
	Line     int    `json:"line,omitempty"`
	Body     string `json:"body"`
	Severity string `json:"severity,omitempty"`
}

// QualityScore is the composite quality assessment for a run result.
type QualityScore struct {
	Correctness  float64 `json:"correctness"`   // 0-1: matched expected comments
	Precision    float64 `json:"precision"`     // 0-1: relevant comments / total comments
	Recall       float64 `json:"recall"`        // 0-1: found expected / total expected
	Speed        float64 `json:"speed"`         // 0-1: normalized latency score
	Efficiency   float64 `json:"efficiency"`    // 0-1: lower LLM calls = better
	HumanQuality float64 `json:"human_quality"` // 0-1: ELO / manual review (initially 0)
	Composite    float64 `json:"composite"`     // weighted average
}

// RunSummary aggregates metrics across all tasks in a run.
type RunSummary struct {
	TotalTasks    int                 `json:"total_tasks"`
	Passed        int                 `json:"passed"`
	Failed        int                 `json:"failed"`
	AvgLatencyMS  float64             `json:"avg_latency_ms"`
	AvgLLMCalls   float64             `json:"avg_llm_calls"`
	AvgTokensUsed float64             `json:"avg_tokens_used"`
	AvgScore      float64             `json:"avg_score"`
	P50LatencyMS  float64             `json:"p50_latency_ms"`
	P90LatencyMS  float64             `json:"p90_latency_ms"`
	P99LatencyMS  float64             `json:"p99_latency_ms"`
	ByMode        map[Mode]*ModeStats `json:"by_mode,omitempty"`
}

// ModeStats aggregates per-mode results for A/B comparison.
type ModeStats struct {
	Mode         Mode    `json:"mode"`
	TotalTasks   int     `json:"total_tasks"`
	Passed       int     `json:"passed"`
	Failed       int     `json:"failed"`
	AvgLatencyMS float64 `json:"avg_latency_ms"`
	AvgLLMCalls  float64 `json:"avg_llm_calls"`
	AvgScore     float64 `json:"avg_score"`
}

// ── ELO Rating ──────────────────────────────────────────────────────────────

// ELORating tracks ELO-style ratings across benchmark runs.
type ELORating struct {
	Mode       Mode      `json:"mode"`
	Rating     float64   `json:"rating"` // current ELO
	Wins       int       `json:"wins"`
	Losses     int       `json:"losses"`
	Draws      int       `json:"draws"`
	LastUpdate time.Time `json:"last_update"`
}
